-- 7. Daily status log
CREATE TABLE `ComputerStatusLog` (
  `RoomID`    VARCHAR(10) NOT NULL,
  `PCNumber`  CHAR(2)     NOT NULL,
  `CheckDate` DATE        NOT NULL,
