-- 9. Trigger (add AFTER seeding to avoid overlap error)
DELIMITER $$
CREATE TRIGGER trg_sync_computer_status
AFTER INSERT ON ComputerStatusLog
FOR EACH ROW
BEGIN
  UPDATE Computers
     SET Status      = IF(NEW.Status='Defective','Defective','Available'),
         LastUpdated = CURDATE()
   WHERE RoomID   = NEW.RoomID
     AND PCNumber = NEW.PCNumber;
END$$
DELIMITER ;

-- 10. Quick pivot check: Yesterday vs Today
SELECT
  c.PCNumber,
  logY.Status AS Yesterday,
  logT.Status AS Today
FROM ( SELECT PCNumber FROM Computers WHERE RoomID='MPO310' AND PCNumber <='40' ) c
LEFT JOIN ComputerStatusLog logY
       ON logY.RoomID='MPO310' AND logY.PCNumber=c.PCNumber
      AND logY.CheckDate = CURDATE()-INTERVAL 1 DAY
LEFT JOIN ComputerStatusLog logT
       ON logT.RoomID='MPO310' AND logT.PCNumber=c.PCNumber
      AND logT.CheckDate = CURDATE()
ORDER BY CAST(c.PCNumber AS UNSIGNED);
